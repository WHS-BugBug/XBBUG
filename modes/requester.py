# request 보내는 requester 함수
def requester(url):
    user_agents = load_user_agents()
    user_agent = random.choice(user_agents)

    headers = {
        "User-Agent": user_agent
    }

    response = requests.get(url, headers=headers, timeout=5)
    
    return response